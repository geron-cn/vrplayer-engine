    CameraCursor* CameraCursor::create(const std::string& imagePath, float width, float height)
    {
        CameraCursor* cursor = new CameraCursor;
        
        if (cursor->init(width, height) == false)
        {
            delete cursor;
            cursor = NULL;
        }
        
        return cursor;
    }
    
    bool CameraCursor::init(float width, float height)
    {
        //left top
        Vector3 lt(-width*0.5f, height*0.5f, 0.f);
        Vector3 lb(-width*0.5f, -height*0.5f, 0.f);
        Vector3 rt(width*0.5f, height*0.5f, 0.f);
        Vector3 rb(width*0.5f, -height*0.5f, 0.f);
        std::vector<float> pos;
        pos.resize(12);
        pos[0] = lt.x;
        pos[1] = lt.y;
        pos[2] = lt.z;
        pos[3] = lb.x;
        pos[4] = lb.y;
        pos[5] = lb.z;
        pos[6] = rt.x;
        pos[7] = rt.y;
        pos[8] = rt.z;
        pos[9] = rb.x;
        pos[10] = rb.y;
        pos[11] = rb.z;
        
        std::vector<float> texCoord;
        texCoord.resize(8);
        texCoord[0]=0;
        texCoord[1]=1;
        texCoord[2]=0;
        texCoord[3]=0;
        texCoord[4]=1;
        texCoord[5]=1;
        texCoord[6]=1;
        texCoord[7]=0;
        
        std::vector<unsigned short> idx;
        idx.resize(6);
        idx[0]=0;
        idx[1]=1;
        idx[2]=2;
        idx[3]=2;
        idx[4]=1;
        idx[5]=3;

        _sprite = Sprite3D::create(pos, texCoord, idx);
        _sprite->setNode(this);
        auto stream = getCursorTextureStream();
        if (stream)
        {
            Texture* textrue = Texture::create(stream);
            _sprite->setTexture(textrue);
            textrue->release();
            stream->close();
            stream->release();
        }
        
        return true;
    }
    
    void CameraCursor::updatePickUp(Node* pickobj)
    {
        if (_state == State::PickingUp)
        {
            if (_pickingObj != pickobj)
            {
                setScale(Vector3(1, 1, 1));
                _pickUpCount = s_pickTotal;
                _pickingObj = pickobj;
            }
            else if (pickobj && pickobj == _pickingObj)
            {
                _pickUpCount--;
                float s = (float)_pickUpCount / s_pickTotal;
                setScale(Vector3(s, s, s));
                if (_pickUpCount <= 0)
                {
                    _state = State::CoolingDown;
                    _pickUpCount = s_pickTotal;
                    _show = false;
                    EventMgr::getInstance()->postEvent(_pickingObj->getName());
                }
            }
            
        }
        else
        {
            _pickUpCount--;
            if (_pickUpCount <= 0)
            {
                _state = State::PickingUp;
                _pickUpCount = s_pickTotal;
                _show = true;
                setScale(Vector3(1.f, 1.f, 1.f));
                EventMgr::getInstance()->removeEvent(_pickingObj->getName());
                _pickingObj = nullptr;
            }
        }
    }
    
    CameraCursor::CameraCursor()
    : _state(State::PickingUp)
    , _show(true)
    , _pickingObj(nullptr)
    {
    }
    
