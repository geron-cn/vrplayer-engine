    void CameraCursor::draw(Camera* camera)
    {
        auto mat = camera->getInverseViewMatrix();
        
        Vector4 offset(0.f, 0.f, -80.f, 1.f);
        offset = mat * offset;
        mat.m[12] = offset.x;
        mat.m[13] = offset.y;
        mat.m[14] = offset.z;
        
        setTransformMatrix(mat);
        
        _isVisible = (mat.m[9] > 0.4);
        
        Node::draw(camera);
//        _sprite->draw(camera);
    }
